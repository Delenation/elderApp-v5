    <div class="news-content">
      <p>暑去秋来，出了三伏，迎来了处暑。处暑，正是由夏入秋的重要时间节点。随着处暑的到来，我们的食欲又被唤醒，无论是传统的贴秋膘，还是秋天的第一茬水果，无不是我们对热烈夏天的告别和对秋天的向往，因为我们离甜蜜的收获时节又近了一步。</p>
      <p>俗话说：不时不食。吃东西要讲究应时应季，水果也一样，枣、梨、葡萄等秋季水果逐渐上市，今天就来为大家介绍这些应季水果的营养价值，以及如何吃才能更养生。</p>
      <p>枣</p>
      <p>水果里的“维生素C大户”</p>
      <p>别看枣个头小，但可以称得上是水果里的“全能选手”，为什么这么说呢？</p>
      <p>1.维生素C含量比柠檬高近十倍</p>
      <p>枣最亮眼的营养“成绩单”就是维生素C。鲜枣中维生素C的含量比大家认知里的“维C大户”柠檬高出了近10倍，一般来说，吃五六个鲜枣就能满足一天的维生素C需要量。</p>
      <p>2.富含多种维生素和矿物质</p>
      <p>除了维生素C，枣的其他营养成分也十分突出，含有对人体有益的胡萝卜素、B族维生素等多种维生素，以及钾、镁、铁等多种矿物质。</p>
      <p>3.含糖量高、热量堪比米饭</p>
      <p>需要注意的是，枣的糖含量很高，每100g枣的能量是125千卡，和一小碗蒸好的米饭能量相当。所以，要想能量不超标，最好仔细数出鲜枣数量，细细品尝为上策。</p>
      <p>梨</p>
      <p>降燥润肺的“首选代言人”</p>
      <p>如果说要为秋天找一种水果来代言，那肯定首先推举梨。</p>
      <p>1.含水量超高</p>
      <p>梨的水分含量很高，各个品种几乎都在 85% 以上，含水量高就能起到润喉、缓解嗓子干燥不适的作用。</p>
      <p>2.有润肠通便的作用</p>
      <p>除了补水，梨还能起到润肠通便的作用，比如来自新疆的库尔勒香梨，它的不溶性膳食纤维的含量达到了6.7g/100g，是一般苹果的近4倍。</p>
      <p>值得注意的是，吃梨也要控制好量，因为有些梨含有较多的山梨糖醇，而山梨糖醇有很强的亲水性，在肠道中具有渗透作用，可以增加粪便的含水量，刺激胃肠蠕动，促进排便。</p>
      <p>3.饭前吃有助于控制食欲</p>
      <p>梨的能量与苹果相当，饭前吃梨，同样有助于控制食欲；在餐桌上，梨也可以有不俗的表现。梨的吃法很多，既能直接生吃，也能蒸、煮或者烤制后食用。既能入汤，也能入药。应季而食，我们常说的银耳雪梨汤可以在这个时节成为家里餐桌上的常客。</p>
      <p>葡萄</p>
      <p>“全身都是宝”</p>
      <p>8月底紧锣密鼓上市的水果还有葡萄。葡萄可以说“全身都是宝”，葡萄果肉、葡萄皮和葡萄籽都拥有对人体有益的成分。</p>
      <p>第一，葡萄含有丰富的维生素C、膳食纤维，以及花青素、类黄酮等植物化学物，还含有苹果酸、柠檬酸等有机酸。</p>
      <p>第二，葡萄中还有一种重要营养素，就是白藜芦醇。白藜芦醇是一种存在于葡萄皮和葡萄籽中的天然多酚，已被证明具有抗氧化、抗炎症等多种益处。</p>
      <p>第三，从营养的角度来看，洗净后，吃葡萄不吐葡萄皮的确是值得推荐的吃法，因为葡萄皮和葡萄籽中都含有白藜芦醇，葡萄皮中的天然色素对降低血糖、抗癌也有良好的效果。</p>
      <p>补充两条秋季饮食小贴士：</p>
      <p>1.推荐黑芝麻、莲子、百合等滋阴润肺的食药物质</p>
      <p>秋季来临，按照传统的观点，这段时间的饮食，还应当注意滋阴润肺，可以适当吃一些具有滋阴作用的食药物质，如黑芝麻、莲子、百合等，少吃辛辣、煎炸、油腻的食物。</p>
      <p>2.避免再摄入太多冷饮以免刺激肠胃</p>
      <p>需要提醒大家的是，在我国北方，初秋的早晚凉意渐浓，昼夜温差加大，空气湿度逐渐降低、气候也变得干燥起来，大家对夏秋之交的冷热变化还没有完全适应，呼吸道疾病、肠胃炎、感冒等疾病在这个时候也容易找上门。在南方地区，秋老虎仍然威力不减，大家还要继续在酷热当中度过一段时间。</p>
      <p>在这段时间千万不要大意，不要再像过夏天一样摄入太多的冷饮，以免刺激肠胃，进而影响身体按照自然气候规律进行自我调适。</p>
      <p>夏末秋初防秋燥 来碗银耳莲子粥吧</p>
      <p>处暑节气刚刚过去，处暑的“处”是指“终止”，处暑的意义是“夏天暑热正式终止”。处暑节气处在天气由热转凉的转换时期，因此我们的饮食、起居也应该进行相应的调整。</p>
      <p>及时适应节气变化</p>
      <p>应多吃蔬果少吃辛热食物</p>
      <p>处暑后的气候变得干燥，容易出现“秋燥”，在饮食上应以清淡为主，新鲜蔬菜和水果不可少。蔬果中含有大量的水分，每天吃点水果有利于防“秋燥”，梨、瓜类都是不错的选择；不宜食用辣椒等辛热食物，更不宜吃烧烤食物。</p>
      <p>入秋想要补充水分</p>
      <p>推荐多喝粥</p>
      <p>通过喝粥补充水分，可以说一举多得。首先，粥大都是以米、面、杂粮为原料，符合《中国居民膳食指南》中“谷类为主，粗细搭配”的原则；其次，粥的包容性强，跟各类食物都可以组合，鸡鸭鱼肉蛋奶都可以加进去；想吃清淡的，可以加蔬菜、水果等；想丰富一点，就加海参鲍鱼山珍海味；想朴素一点，就加地瓜山药土豆，很容易实现食物多样；第三，煮粥用的杂粮，其谷类表层所含的维生素、矿物质等营养素和膳食纤维都会全部保留下来，避免了营养物质的流失。</p>
      <p>银耳、百合、莲子也可以单独和谷类食物放在一起煮成粥，不仅可以提供蛋白质、碳水化合物，还有维生素和矿物质，同时补充了不少水分。</p>
      <p>银耳能补肾、润肺、生津、止咳</p>
      <p>所含多糖帮助降血脂、抗肿瘤</p>
      <p>处暑时节，推荐大家煮点银耳百合莲子羹。银耳百合莲子羹的做法非常简单，把银耳用温水泡发撕成小片，莲子、百合和枸杞分别用温水泡发。将银耳煮至浓稠后，放入冰糖搅拌均匀、然后倒入莲子，盖上锅盖小火煮半小时，最后放入百合和枸杞再煮15分钟左右即可熄火。</p>
      <p>想喝凉爽的，将煮好的羹放入冰箱，口感非常好。</p>
      <p>银耳又称白木耳，含有丰富的蛋白质、碳水化合物、膳食纤维、维生素和矿物质，是一种营养丰富的食用菌，常用于做汤或甜品。</p>
      <p>研究表明，银耳中富含的银耳多糖具有提高免疫力、抗衰老、降血糖、降血脂、抗肿瘤等作用。</p>
      <p>另外，中医认为，银耳能补肾、润肺、生津、止咳。因此，银耳一直以来都被视为养生保健和美容养颜佳品；莲子也具有补心益脾,养血安神等功效。</p>
